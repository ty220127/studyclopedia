//Numbering-Rule
function nr(model,box) {
  document.write("[#" + model + "-" + box + "]")
}

function athr(n){
		if(authors[n][2] == null){
			document.write(authors[n][1]);
		} else {
			if(authors[n][4] == null){
				document.write(authors[n][1] + " and " + authors[n][3]);
			} else {
				document.write(authors[n][1] + " et al.");
			}
		}
}

function athr2(n){
	al = authors[n].length;
	if(al == 2){
		document.write(authors[n][0] + " " + authors[n][1]);
	}else if(al == 4){
			document.write(authors[n][0] + " " + authors[n][1] + " and " + authors[n][2] + " " + authors[n][3]);
	} else {
		for(i = 0; 2*i < authors[n].length-2; i++){
			document.write(authors[n][2*i] + " " + authors[n][2*i +1] + ", ");
		}
		document.write(" and " + authors[n][authors[n].length-2] + " " + authors[n][authors[n].length-1]);
	}
}
